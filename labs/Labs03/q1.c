#include <stdio.h>
int main()    {
                int num1,num2;
                char op;
                printf("enter 1st number: ");
                scanf("%d" , &num1);
                printf("enter 2nd number: ");
                scanf("%d" , &num2);
                printf("enter any operator +,-,*,/: ");
                scanf(" %c"  ,&op);
                if( op == '+'){
                    printf("addition of numbers is: %d", num1+num2);
                }
                else if( op == '-') {
                    printf("subtraction of numbers is: %d", num1-num2);
                } 
                else if( op == '/') {
                    printf("division of numbers is: %d", num1/num2);
                }
                else if( op == '*') {
                    printf("multiplication of numbers is: %d", num1*num2);
                }
                else{
                    printf("invalid input");
                }

}
